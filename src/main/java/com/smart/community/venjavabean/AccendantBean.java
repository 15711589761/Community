package com.smart.community.venjavabean;

public class AccendantBean
{
	private int accendantId;
	private String accendantName;
	private String accendantTel;
	private String accendantAge;
	private int accendantSex;

	public int getAccendantId()
	{
		return accendantId;
	}

	public void setAccendantId(int accendantId)
	{
		this.accendantId = accendantId;
	}

	public String getAccendantName()
	{
		return accendantName;
	}

	public void setAccendantName(String accendantName)
	{
		this.accendantName = accendantName;
	}

	public String getAccendantTel()
	{
		return accendantTel;
	}

	public void setAccendantTel(String accendantTel)
	{
		this.accendantTel = accendantTel;
	}

	public String getAccendantAge()
	{
		return accendantAge;
	}

	public void setAccendantAge(String accendantAge)
	{
		this.accendantAge = accendantAge;
	}

	public int getAccendantSex()
	{
		return accendantSex;
	}

	public void setAccendantSex(int accendantSex)
	{
		this.accendantSex = accendantSex;
	}
}
